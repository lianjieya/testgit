	public void paint(Graphics g){
		super.paintComponent(g);
		g.setColor(Color.BLACK);
		for(int i=0;i<row-2;i++){
			g.drawLine(0, i*size, 480*2, i*size);
		}
		for(int j=0;j<col-2;j++){
			g.drawLine(j*size, 0, j*size,380*2);
		}
		for(int i=1;i<row-1;i++){
			for(int j=1;j<col-1;j++){
				if(oldMap[i][j]==1){
					g.fillRect((j-1)*size,(i-1)*size, size, size);
				}
			}
		}
	}
	
	

	public void update(){
		int[][] newMap=new int[row][col];

	
		for(int i=1;i<row-1;i++)
		{
			for(int j=1;j<col-1;j++)
			{
				
			    //数出周围有几个细胞生
			    int life=0;
			    life=oldMap[i-1][j-1]+oldMap[i-1][j]+oldMap[i-1][j+1]
			         +oldMap[i][j-1]+oldMap[i][j+1]
			         +oldMap[i+1][j-1]+oldMap[i+1][j]+oldMap[i+1][j+1];
			    switch(life){
			    case 2:
			    	newMap[i][j]=oldMap[i][j];
			    	break;
			    case 3:
			    	newMap[i][j]=1;
			    	break;
			    default:
			    	newMap[i][j]=0;
			    	break;
			    }
			    
			       
			                
			    }
			  
			}
			
			
			oldMap=newMap;
			
	}

